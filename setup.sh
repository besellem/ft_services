}


# If no arg, or first arg equals "start" or "restart":
# Start / Restart script
if [ $# -lt 1 ] || [ $1 = "start" ]
then
	start
elif [ $1 = "delete" ] || [ $1 = "stop" ]
then
	# Delete minikube instances
	if [ -x `command -v minikube` ]
	then
		minikube delete
	fi
elif [ $1 = "services" ]
then

	# check if minikube is working
	minikube ip
	if [ $? -ne 0 ]
	then
		start
	fi
	
	# Launch services
	setup
elif [ $1 = "install" ]
then
	# Install minikube
	install_minikube_cmd
fi

